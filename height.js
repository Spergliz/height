function checkHeight(height) {
  if (height < 82) {
    return "Too short for ride";
  } else {
    return "All Aboard!";
  }
}

function analyzeNumber(number) {
  if (number > 0) {
    return "postive number";
  } else if (number < 0) {
    return "negative number";
  } else {
    return "zero";
  }
}

